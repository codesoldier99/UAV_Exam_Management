<template>
  <router-view />
</template>

<script setup>
import { onMounted } from 'vue'
import { useUserStore } from '@/stores/user'

const userStore = useUserStore()

onMounted(() => {
  // Check if user is logged in
  const token = localStorage.getItem('token')
  if (token) {
    userStore.token = token
    userStore.fetchUserInfo()
  }
})
</script>

